print(f'A Multiplicação do {N1} e {N2} é igual a : {N1*N2}')
print(f'A Divição inteira do {N1} e {N2} é igual a : {N1//N2}')
print(f'A Potencia do {N1} e {N2} é igual a : {N1**N2}')